    private void jButton_CE_GraficarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_CE_GraficarMouseClicked
        try
        {
            // Variables
            Datos_CampoEscalar datosCampoEscalar=new Datos_CampoEscalar();
            String ecuacionCampoEscalar="";//((x^2)/(1^2))+((y^2)/(2^2))-1
            double incremento=0.01;//0.01
            double ladoDeCuadrado=1;
            double ladoDeCubo=1;
            double escala=1;
            boolean usaConstanteEspecifica=false;
            double valorConstanteEspecifica=0.0;

            // Recuperamos incremento
            String txtIncremento="";
            txtIncremento=""+this.jComboBox_CE_incremento.getSelectedItem();
            if(txtIncremento.equalsIgnoreCase("Bajo"))
            {
                incremento=Principal_Modelo.Incremento_DivisionesBajo;
            }
            if(txtIncremento.equalsIgnoreCase("Medio"))
            {
                incremento=Principal_Modelo.Incremento_DivisionesMedio;
            }
            if(txtIncremento.equalsIgnoreCase("Cargado"))
            {
                incremento=Principal_Modelo.Incremento_DivisionesCargado;
            }
            if(txtIncremento.equalsIgnoreCase("Máximo"))
            {
                incremento=Principal_Modelo.Incremento_DivisionesMaximo;
            }

            // Recuperamos demás datos
            ecuacionCampoEscalar=this.jTextField_CE_ecuacionCampoEscalar.getText();
            ladoDeCubo=ladoDeCuadrado=Double.parseDouble(this.jTextField_CE_Rango.getText());
            escala=Double.parseDouble(this.jTextField_CE_Escala.getText());
            usaConstanteEspecifica=this.jComboBox_CE_UsarConstante.isSelected();//Cte.
            valorConstanteEspecifica=Double.parseDouble(this.jTextField_CE_ValorConstante.getText());//Cte.
            
            // Comprobamos datos
            if(ladoDeCubo<=0||ladoDeCuadrado<=0||escala<=0)
            {
                JOptionPane.showMessageDialog(null, "Verifique los datos que ha ingresado.", "ERROR AL INTENTAR GRAFICAR.", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Insertamos datos
            datosCampoEscalar.setEcuacionCampoEscalar(ecuacionCampoEscalar);
            datosCampoEscalar.setIncremento(incremento);
            datosCampoEscalar.setLadoDeCubo(ladoDeCubo);
            datosCampoEscalar.setLadoDeCuadrado(ladoDeCuadrado);
            datosCampoEscalar.setEscala(escala);
            datosCampoEscalar.setUsaConstanteEspecifica(usaConstanteEspecifica);//Cte.
            datosCampoEscalar.setValorConstanteEspecifica(valorConstanteEspecifica);//Cte.
            
            // Mandamos a graficar
            this.objPrincipal_Modelo.esCampoEscalar(datosCampoEscalar);
            this.invocar_GUIVentanaGraficas_General(this, this.jButton_CE_Graficar);
        }
        catch(java.lang.NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "Verifique los datos que ha ingresado.", "ERROR AL INTENTAR GRAFICAR.", JOptionPane.ERROR_MESSAGE);
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, "Verifique los datos que ha ingresado.", "ERROR AL INTENTAR GRAFICAR.", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton_CE_GraficarMouseClicked

